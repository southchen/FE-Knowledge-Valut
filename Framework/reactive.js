/**
 * Parse simple path.
 */
var bailRE = new RegExp('[^' + unicodeRegExp.source + '.$_\\d]');
function parsePath(path) {
  if (bailRE.test(path)) {
    return;
  }
  var segments = path.split('.');
  return function (obj) {
    for (var i = 0; i < segments.length; i++) {
      if (!obj) {
        return;
      }
      obj = obj[segments[i]];
    }
    return obj;
  };
}
